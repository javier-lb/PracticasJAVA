public class WhileBucle01{
		public static void main(String [] args){
			int numero=1;
			int multiplo=0;
			int total=0;

			while (total <=95){
				total = numero * multiplo;
				System.out.println(numero + "*" + multiplo + "=" + total);
				multiplo = multiplo +5;
				}
			
	}
	//Tabla de multiplicar del 5 con while 
}